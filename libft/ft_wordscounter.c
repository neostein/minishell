int		ft_wordscounter(char *s, char c)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (s[i])
	{
		if (s[i] == c)
		{
			j++;
			while (s[i] && s[i + 1] == c)
				i++;
		}
		i++;
	}
	return (j + 1);
}
