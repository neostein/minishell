int	ft_atoi(const char *str)
{
	int i;
	int k;
	int sign;

	k = 0;
	i = 0;
	sign = 1;
	while (str[i] == '\t' || str[i] == '\n' ||
			str[i] == ' ' || str[i] == '\f' || str[i] == '\r' || str[i] == '\v')
		i++;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] != '\0' && (str[i] >= '0' && str[i] <= '9'))
	{
		if (str[i - 1] == '-')
			sign = -1;
		k = k * 10 + (str[i] - '0');
		i++;
	}
	return (k * sign);
}
